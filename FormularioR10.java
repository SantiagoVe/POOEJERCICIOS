        double mat,pat;
        String nombre;
        ni=Integer.parseInt(NI.getText());
        est=Integer.parseInt(EST.getText());
        pat=Double.parseDouble(PAT.getText());
        nombre=NOMBRE.getText();
        if (pat>2000000 && est>3){
            mat = (pat*0.03)+50000;
            MAT.setText(String.valueOf(mat));
        }
        else{
            MAT.setText("50000");
        }
